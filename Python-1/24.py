a = [ "Amrit" , "Alok" , "Anurag" , "Anjali"]

b = 0 
while b<len(a):                   # Until this condition is satisfied loop keeps looping.
    print(a[b])                   # This will print b indexed item from list a.
    b = b + 1                     #As the value of b keeps increasing loop keeps printing more indexed items.