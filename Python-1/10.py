t = (1,3,2,4,5,6,5,3,1)        # Tuple is as same as List but it cannot be modified.
print(t)                       # Tuple is in small brackets.

print(t.count(1))              # It will count no. of items named 1 in the tuple  
print(t.index(3))              # It will print the index of item named 3.        
